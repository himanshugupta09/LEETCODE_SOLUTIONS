class Solution {
  public:
    long long int minOperations(int N) {
        // Code here
        if(N == 1)
        {
            return 1;
        }
        long long int cn = N;
        long long int hi = N/2;
        
        long long int res = hi*cn-hi*hi;
        return res;
    }
};

