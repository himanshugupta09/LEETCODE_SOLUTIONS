class Solution{
    //Function to find the leaders in the array.
    public:
    vector<int> leaders(int a[], int n){
        // Code here
        vector<int>res;
        res.push_back(a[n-1]);
        int max_red = a[n-1];
        for(int i=2;i<=n;i++)
        {
            if(a[n-i] >= max_red)
            {
                max_red = a[n-i];
                res.push_back(max_red);
            }
        }
        reverse(res.begin(),res.end());
        return res;
        
    }
};

