class Solution {
  public:
    int getSingle(vector<int> &arr) {
        // code here
        int ones = 0,two = 0;
        for(auto i:arr)
        {
            ones = (ones^i)&(~two);
            two = (two^i)&(~ones);
        }
        return ones;
    }
};


