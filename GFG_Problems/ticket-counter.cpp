class Solution {
  public:
    int distributeTicket(int N, int K) {
        // code here
        
        int se = ceil((float)N/K);
        
        if(se%2 == 1)
        {
            return N-se/2*K;
        }
        else
        {
            return se/2*K+1;
        }
    }
};


