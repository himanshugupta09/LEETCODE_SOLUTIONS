/* ===============================================================================================================================================================
                                                        BRUTE FORCE APPROACH
===================================================================================================================================================================
*/
class Solution {
public:
    
    float logn(int n,int r)
    {
        return log(n)/log(r);
    }
    
    
    
    bool isPowerOfThree(int n) {
        
        if(n < 1)
        {
            return 0;
        }
        else{
            while(n % 3 == 0)
            {
                n /= 3;
                
            }
            return n == 1;
        }
        
    }
};

