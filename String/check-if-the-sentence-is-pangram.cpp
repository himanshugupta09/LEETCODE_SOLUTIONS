/*
====================================================================================================================================================================
                                                     APPPROACH 1 USING THE HASHSET
====================================================================================================================================================================
*/
class Solution {
public:
    bool checkIfPangram(string sentence) {
        
        unordered_set<char>seen(sentence.begin(),sentence.end());
        
        return seen.size() == 26;
        
        
        
    }
};

/*
=====================================================================================================================================================================
                                                        APPROACH 2 USING THE BIT MANIPULATION 
=====================================================================================================================================================================
*/

class Solution {
public:
    bool checkIfPangram(string sentence) {
        
        
        int seen=  0;
        
        for(auto now:sentence)
        {
            int rel = now-'a';
            
            int nowBit = 1<<rel;
            
            seen |= nowBit;
            
        }
        
        
        
        return seen == (1 << 26) - 1;
        
        
        
        
    }
};
