class Solution {
  public:
    // Function to find maximum product subarray
    int maxProduct(vector<int> &arr) {
        // Your Code Here
        int res = INT_MIN;
    int l=1;
    int r = 1;
    int n = arr.size();
    for(int i=0;i<n;i++)
    {
        l *= arr[i];
        r *= arr[n-1-i];
        res = max(res,max(l,r));
        if(l==0) l=1;
        if(r==0) r = 1;
        
    }
    return res;
    }
};

